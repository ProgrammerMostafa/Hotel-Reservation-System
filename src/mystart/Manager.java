    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       PreparedStatement d,dd;
       if(emp.getSelectedRow()!=-1)
       {
        try {
            d = help.getConnected().prepareStatement("delete from employee where Emp_id = ? ");
            int index=emp.getSelectedRow();
            d.setString(1,emp.getValueAt(index, 0).toString());
            d.executeUpdate();
        } catch (SQLException ex) {
            Logger.getLogger(Manager.class.getName()).log(Level.SEVERE, null, ex);
        }
       }
        else if (rec.getSelectedRow() != -1) 
        {
            try {
                dd = help.getConnected().prepareStatement("delete from receptionist where ID = ? ");
                int i = rec.getSelectedRow();
                dd.setString(1, rec.getValueAt(i, 0).toString());
                dd.executeUpdate();
            } catch (SQLException ex) {
                Logger.getLogger(Manager.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
       else
            JOptionPane.showMessageDialog(null,"Please Select a Row","Error",JOptionPane.ERROR_MESSAGE);
               
    }//GEN-LAST:event_jButton2ActionPerformed


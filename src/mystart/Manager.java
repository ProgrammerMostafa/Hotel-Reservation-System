    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        try {
                /////////////// Show Employee information:
                PreparedStatement d=help.getConnected().prepareStatement("select * from employee ");
                ResultSet r=d.executeQuery();
                DefaultTableModel dm=new DefaultTableModel();
                dm.addColumn("Emp_ID");
                dm.addColumn("First Name");
                dm.addColumn("Middle Name");
                dm.addColumn("Last Name");
                dm.addColumn("Address");
                dm.addColumn("Salary");
                dm.addColumn("Position");
                dm.addColumn("Gender");
                while(r.next())
                {
                    String arr[]={r.getString(4) , r.getString(1) , r.getString(2) , r.getString(3) , 
                    r.getString(5) , r.getString(6) , r.getString(7) , r.getString(8) };
                    dm.addRow(arr);
                }
                emp.setModel(dm);
                
                ////////////// Show Receptionist Information : 
                PreparedStatement dd=help.getConnected().prepareStatement("select * from receptionist ");
                ResultSet rr=dd.executeQuery();
                DefaultTableModel fm=new DefaultTableModel();
                fm.addColumn("Emp_ID");
                fm.addColumn("First Name");
                fm.addColumn("Middle Name");
                fm.addColumn("Last Name");
                fm.addColumn("Address");
                fm.addColumn("Salary");
                fm.addColumn("Gender");
                fm.addColumn("Pass");
                while(rr.next())
                {
                    String arr[]={ rr.getString(1) , rr.getString(2) , rr.getString(3) , rr.getString(4) , 
                    rr.getString(5) , rr.getString(6) , rr.getString(7) , rr.getString(8) };
                    fm.addRow(arr);
                }
                rec.setModel(fm);
                
            } catch (SQLException ex) {
                Logger.getLogger(Rooms.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_formWindowOpened

